package lesExceptions;

public class ErrorKeyValueException extends Exception {

	public ErrorKeyValueException() {
	}

	public ErrorKeyValueException(String arg0) {
		super(arg0);
	}

	public ErrorKeyValueException(Throwable arg0) {
		super(arg0);
	}

	public ErrorKeyValueException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public ErrorKeyValueException(String arg0, Throwable arg1, boolean arg2,
			boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
